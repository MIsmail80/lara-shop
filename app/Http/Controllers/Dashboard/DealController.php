     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $inputs = request()->all();

        $startTime = Carbon::parse($inputs['started_at']);

        $inputs['end_at'] = $startTime->addHours($inputs['duration']);

        $product = Product::find($inputs['product_id']);

        $inputs['amount'] = $product->price * $inputs['discount'] / 100;

        $newDeal = Deal::create($inputs);

        return back()->with('success', 'The deal has been saved.');
    }

